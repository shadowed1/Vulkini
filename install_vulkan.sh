sudo apt install -y --no-upgrade --no-install-recommends libclang-19-dev
sudo apt install -y --no-upgrade --no-install-recommends llvm-19-dev 
sudo apt install -y --no-upgrade --no-install-recommends llvm-spirv-19

sudo apt upgrade -y

cd
rm -rf mesa-* 2>/dev/null
LATEST=$(curl -s https://archive.mesa3d.org/ \
  | grep -oE 'mesa-[0-9]+\.[0-9]+\.[0-9]+\.tar\.xz' \
  | grep -v 'rc' \
  | sort -V \
  | tail -n1)
wget "https://archive.mesa3d.org/$LATEST"
tar xf "$LATEST"
cd "${LATEST%.tar.xz}"
rm -rf build64 2>/dev/null
ARCH="$(uname -m)"

case "$ARCH" in
    x86_64)
        LIBDIR="lib/x86_64-linux-gnu"

        # Removing specific GPU support
        CPU_VENDOR=$(grep -m1 "vendor_id" /proc/cpuinfo | awk -F: '{print $2}' | xargs)
        case "$CPU_VENDOR" in
            GenuineIntel)
                VULKAN_DRIVERS="virtio"
                # intel,virtio
                ;;
            AuthenticAMD)
                VULKAN_DRIVERS="virtio"
                # amd,virtio
                ;;
            *)
                echo "${YELLOW}Unknown x86 CPU vendor: $CPU_VENDOR. Defaulting to virtio.${RESET}"
                VULKAN_DRIVERS="virtio"
                ;;
        esac
        ;;
    aarch64)
        LIBDIR="lib/aarch64-linux-gnu"
        VULKAN_DRIVERS="virtio"
        ;;
    *)
        echo "${RED}Unsupported arch: $ARCH ${RESET}"
        sleep 5
        exit 1
        ;;
esac

# sudo mkdir -p /opt/mesa
meson setup build64 \
    --libdir "$LIBDIR" \
    --wrap-mode=nofallback \
    -Dprefix=/usr \
    -Dplatforms=x11,wayland \
    -Dvulkan-drivers="$VULKAN_DRIVERS" \
    -Dgallium-drivers=virgl,zink \
    -Dglx=dri \
    -Degl=enabled \
    -Dgbm=enabled \
    -Dgles1=enabled \
    -Dgles2=enabled \
    -Dspirv-tools=enabled \
    -Dgallium-rusticl=false \
    -Dvideo-codecs=all \
    -Dgallium-d3d12-video=enabled \
    -Dgallium-d3d12-graphics=enabled \
    -Dvalgrind=enabled
sudo ninja -C build64 install

ARCH="$(uname -m)"

if [ "$ARCH" = "x86_64" ]; then
    sudo cp build64/src/compiler/clc/mesa_clc /bin/ 2>/dev/null
    sudo cp build64/src/compiler/spirv/vtn_bindgen2 /bin/ 2>/dev/null
    sudo chmod +x /bin/mesa_clc 2>/dev/null
    sudo chmod +x /bin/vtn_bindgen2 2>/dev/null

    mkdir -p ~/.local/share/meson/cross

    cat > ~/.local/share/meson/cross/i686-cross.ini << 'EOF'
[binaries]
c = '/usr/bin/gcc'
cpp = '/usr/bin/g++'
