#!/bin/bash

RED=$(tput setaf 1)
GREEN=$(tput setaf 2)
YELLOW=$(tput setaf 3)
BLUE=$(tput setaf 4)
MAGENTA=$(tput setaf 5)
CYAN=$(tput setaf 6)
BOLD=$(tput bold)
RESET=$(tput sgr0)

echo
echo "${GREEN}In ChromeOS, make sure to go to ${BOLD}chrome://flags ${RESET}${GREEN}and enable ${BOLD}Crostini GPU${RESET}${GREEN} flag! ${RESET}"
echo

cleanup() {
    echo
    echo "${YELLOW}Exiting Crostini Vulkan setup...${RESET}"
}
trap cleanup EXIT

read -rp "${CYAN}Do you want to Install Steam + Prismlauncher for Crostini? ${BOLD}(y/N)${RESET}${CYAN}: ${RESET}" BUILD_CHOICE
case "$BUILD_CHOICE" in
    y|Y|yes|YES|Yes)
        echo
        echo "${GREEN}Proceeding to installation...${RESET}"
        sleep 1
        echo
        ;;
    *)
        echo
        echo "${RED}Exiting setup.${RESET}"
        sleep 2
        exit 0
        ;;
esac

sudo apt -o Acquire::ForceIPv4=true update
echo
echo "${BLUE}Adding ${BOLD}$USER${RESET}${BLUE} to groups to enable GPU acceleration: ${RESET}${CYAN}"
echo
getent group 44 || groupadd -g 44 video
sudo gpasswd -a $USER video
sudo gpasswd -a $USER render
sudo gpasswd -a $USER plugdev
echo "${RESET}"

sudo tee /etc/apt/sources.list > /dev/null <<'EOF'
# Generated by distrobuilder
deb https://deb.debian.org/debian trixie main contrib non-free non-free-firmware
deb https://deb.debian.org/debian trixie-updates main contrib non-free non-free-firmware
deb https://deb.debian.org/debian-security trixie-security main contrib non-free non-free-firmware
deb-src https://deb.debian.org/debian trixie main contrib non-free non-free-firmware
EOF

if [ "$(uname -m)" != "aarch64" ]; then
    sudo dpkg --add-architecture i386
        if ! command -v steam >/dev/null 2>&1; then
        echo
        read -rp "${CYAN}Steam is not installed. Install Steam? ${BOLD}(y/N)${RESET}${CYAN}: ${RESET}" STEAM_CHOICE
        echo
    
        case "$STEAM_CHOICE" in
            y|Y|yes|YES|Yes)
                sudo apt -o Acquire::ForceIPv4=true update
                sudo apt install -y vulkan-tools mesa-utils vulkan-validationlayers steam-installer
                ;;
            *)
                echo
                echo "${YELLOW}Skipping Steam installation.${RESET}"
                echo
                ;;
        esac
    fi
fi

echo "${BLUE}"
sudo apt -o Acquire::ForceIPv4=true update
echo "${RESET}"
sudo apt install -y vulkan-tools mesa-utils vulkan-validationlayers
sudo apt install flatpak -y
flatpak --user remote-add --if-not-exists flathub https://dl.flathub.org/repo/flathub.flatpakrepo
sudo curl -fsSL "https://raw.githubusercontent.com/shadowed1/Chard/main/bin/vulkan_tester.sh" -o "/bin/vulkan_tester" 2>/dev/null
sleep 0.2
sudo chmod +x /bin/vulkan_tester 2>/dev/null
vulkan_tester 2>/dev/null
echo "${RESET}"
