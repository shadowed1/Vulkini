#!/bin/bash

RED=$(tput setaf 1)
GREEN=$(tput setaf 2)
YELLOW=$(tput setaf 3)
BLUE=$(tput setaf 4)
MAGENTA=$(tput setaf 5)
CYAN=$(tput setaf 6)
BOLD=$(tput bold)
RESET=$(tput sgr0)

echo
echo "${GREEN}In ChromeOS, make sure to go to ${BOLD}chrome://flags ${RESET}${GREEN} and enable ${BOLD}Crostini GPU${RESET}${GREEN} flag! ${RESET}"
echo "${CYAN}Starting Vulkan install in 10 seconds... ${RESET}"
echo
sleep 8

sudo apt update
echo
echo "${BLUE}Adding ${BOLD}$USER${RESET}${BLUE} to groups to enable GPU acceleration: ${RESET}${CYAN}"
echo
getent group 44 || groupadd -g 44 video
sudo gpasswd -a $USER video
sudo gpasswd -a $USER render
sudo gpasswd -a $USER plugdev
echo "${RESET}"

sudo tee /etc/apt/sources.list > /dev/null <<'EOF'
# Generated by distrobuilder
deb https://deb.debian.org/debian trixie main contrib non-free non-free-firmware
deb https://deb.debian.org/debian trixie-updates main contrib non-free non-free-firmware
deb https://deb.debian.org/debian-security trixie-security main contrib non-free non-free-firmware
deb-src https://deb.debian.org/debian trixie main contrib non-free non-free-firmware
EOF

if [ "$(uname -m)" != "aarch64" ]; then
    sudo dpkg --add-architecture i386
fi

sudo apt update
sudo apt install -y \
vulkan-tools libepoxy-dev libvulkan-dev python3-yaml git meson pkg-config \
clang libclang-dev zstd tar mesa-utils cmake lua5.4 liblua5.4-dev \
vulkan-validationlayers libunwind-dev hwdata llvm llvm-dev \
libdisplay-info-dev valgrind libglvnd-dev glslang-tools libva-dev \
python3-mako zlib1g-dev libzstd-dev libexpat1-dev libdrm-dev byacc \
libudev-dev libelf-dev bison flex wayland-protocols \
libwayland-egl-backend-dev libxcb-randr0-dev libxext-dev \
libxcb-glx0-dev libxcb-shm0-dev libx11-xcb-dev libxcb-keysyms1-dev \
libxcb-dri3-dev libxcb-present-dev libxshmfence-dev libxxf86vm-dev \
libxrandr-dev libclc-19-dev llvm-19-dev llvm-spirv-19 \
libllvmspirvlib-19-dev clang-19 libclang-19-dev

cd
rm -rf mesa-* 2>/dev/null
LATEST=$(curl -s https://archive.mesa3d.org/ \
  | grep -oE 'mesa-[0-9]+\.[0-9]+\.[0-9]+\.tar\.xz' \
  | grep -v 'rc' \
  | sort -V \
  | tail -n1)
wget "https://archive.mesa3d.org/$LATEST"
tar xf "$LATEST"
cd "${LATEST%.tar.xz}"
rm -rf build64 2>/dev/null
ARCH="$(uname -m)"

if [ "$ARCH" = "aarch64" ]; then
    VULKAN_DRIVERS="virtio"
else
    VULKAN_DRIVERS="intel,virtio"
fi

meson setup build64 \
    --libdir /usr/lib/x86_64-linux-gnu \
    --wrap-mode=nofallback \
    -Dprefix=/usr \
    -Dplatforms=x11,wayland \
    -Dvulkan-drivers="$VULKAN_DRIVERS" \
    -Dgallium-drivers=virgl,zink \
    -Dglx=dri \
    -Degl=enabled \
    -Dgbm=enabled \
    -Dgles1=enabled \
    -Dgles2=enabled \
    -Dspirv-tools=enabled \
    -Dgallium-rusticl=false \
    -Dvideo-codecs=all \
    -Dgallium-d3d12-video=enabled \
    -Dgallium-d3d12-graphics=enabled \
    -Dvalgrind=enabled

sudo ninja -C build64 install

ARCH="$(uname -m)"

if [ "$ARCH" = "x86_64" ]; then
    sudo cp build64/src/compiler/clc/mesa_clc /bin/ 2>/dev/null
    sudo cp build64/src/compiler/spirv/vtn_bindgen2 /bin/ 2>/dev/null
    sudo chmod +x /bin/mesa_clc 2>/dev/null
    sudo chmod +x /bin/vtn_bindgen2 2>/dev/null

    mkdir -p ~/.local/share/meson/cross

    cat > ~/.local/share/meson/cross/i686-cross.ini << 'EOF'
[binaries]
c = '/usr/bin/gcc'
cpp = '/usr/bin/g++'
ar = '/usr/bin/gcc-ar'
strip = '/usr/bin/strip'
pkg-config = '/usr/bin/i686-linux-gnu-pkg-config'
llvm-config = '/usr/bin/llvm-config'
cmake = '/usr/bin/cmake'

[built-in options]
c_args = ['-m32']
c_link_args = ['-m32']
cpp_args = ['-m32']
cpp_link_args = ['-m32']

[host_machine]
system = 'linux'
cpu_family = 'x86'
cpu = 'i686'
endian = 'little'
EOF

    sudo apt install -y \
    gcc-multilib g++-multilib \
    pkg-config:i386 libdrm-dev:i386 libwayland-dev:i386 \
    libwayland-egl-backend-dev:i386 libxext-dev:i386 libxfixes-dev:i386 \
    x11proto-dev:i386 libxcb-glx0-dev:i386 libxcb-shm0-dev:i386 \
    libx11-xcb-dev:i386 libxcb-dri2-0-dev:i386 libxcb-dri3-dev:i386 \
    libxcb-present-dev:i386 libxshmfence-dev:i386 libxxf86vm-dev:i386 \
    libxrandr-dev:i386 libunwind-dev:i386 libelf-dev:i386 \
    libzstd-dev:i386 libbsd-dev:i386 libsensors-dev:i386 \
    libxcb-keysyms1-dev:i386 libva-dev:i386 libvulkan-dev:i386 \
    libglvnd-dev:i386 libexpat1-dev:i386 zlib1g-dev:i386 \
    libx11-dev:i386 libxcb-randr0-dev:i386 libxcb1-dev:i386 \
    libxcb-sync-dev:i386 libxcb-xfixes0-dev:i386 libxdamage-dev:i386

    rm -rf build32 2>/dev/null

    meson setup build32 \
        --cross-file ~/.local/share/meson/cross/i686-cross.ini \
        --wrap-mode=nofallback \
        -Dprefix=/usr \
        -Dlibdir=lib/i386-linux-gnu \
        -Dplatforms=x11,wayland \
        -Dvulkan-drivers=intel,virtio \
        -Dgallium-drivers=virgl,zink \
        -Dglx=dri \
        -Degl=enabled \
        -Dgbm=enabled \
        -Dgles1=enabled \
        -Dgles2=enabled \
        -Dllvm=disabled \
        -Dmesa-clc=system \
        -Dspirv-tools=disabled \
        -Dgallium-rusticl=false \
        -Dvideo-codecs=all \
        -Dgallium-d3d12-video=enabled \
        -Dgallium-d3d12-graphics=enabled \
        -Dvalgrind=disabled

    sudo ninja -C build32 install
fi

rm -rf mesa-* 2>/dev/null
vulkaninfo --summary
